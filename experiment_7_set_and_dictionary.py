"""Write a program to count the frequency of characters in a user-given string using a dictionary."""

s = input("Enter a string: ")
f = {}
for c in s:
    f[c] = f.get(c, 0) + 1
print("Character frequencies:", f)

"""Write a program to merge two dictionaries into one."""

x = {}
y = {}
print("Enter 2 key-value pairs for first dictionary:")
for _ in range(2):
    k = input("Key: ")
    v = int(input("Value: "))
    x[k] = v
print("Enter 2 key-value pairs for second dictionary:")
for _ in range(2):
    k = input("Key: ")
    v = int(input("Value: "))
    y[k] = v
z = x.copy()
z.update(y)
print("Merged dictionary:", z)

