"""Write a program to take two sets of integers and perform the following operations: Union,
intersection, difference, and symmetric difference.
"""

a = set(map(int, input("Enter elements of first set separated by space: ").split()))
b = set(map(int, input("Enter elements of second set separated by space: ").split()))
print("Union:", a | b)
print("Intersection:", a & b)
print("Difference:", a - b)
print("Symmetric Difference:", a ^ b)

